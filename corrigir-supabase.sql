create or replace function public.usuario_eh_admin()
returns boolean
language sql
stable
security definer
set search_path = public
set row_security = off
as $$
  select exists (
    select 1 from public.usuarios
    where id = auth.uid() and role = 'admin'
  );
$$;

-- O painel usa RPCs security definer para listar donos e alterar os dois lados
-- do vínculo em uma única transação, sem liberar clube_id para updates comuns.
create or replace function public.admin_listar_donos()
returns table (
  id uuid,
  email text,
  nome text,
  role text,
  clube_id bigint
)
language sql
stable
security definer
set search_path = public
set row_security = off
as $$
  select u.id, u.email, u.nome, u.role, u.clube_id
  from public.usuarios u
  where public.usuario_eh_admin()
    and u.role = 'dono'
  order by u.nome, u.email;
$$;

create or replace function public.admin_associar_clube_dono(
  p_clube_id bigint,
  p_usuario_id uuid
)
returns void
language plpgsql
security definer
set search_path = public
set row_security = off
as $$
begin
  if not public.usuario_eh_admin() then
    raise exception 'Acesso restrito ao administrador'
      using errcode = '42501';
  end if;

  if not exists (select 1 from public.clubes where id = p_clube_id) then
    raise exception 'Clube não encontrado';
  end if;

  if p_usuario_id is not null and not exists (
    select 1 from public.usuarios
    where id = p_usuario_id and role = 'dono'
  ) then
    raise exception 'Perfil de dono não encontrado';
  end if;

  -- Libera o dono anterior deste clube e o clube anterior do novo dono.
  update public.usuarios
  set clube_id = null
  where clube_id = p_clube_id;

  if p_usuario_id is not null then
    update public.clubes
    set usuario_dono_id = null
    where usuario_dono_id = p_usuario_id;
  end if;

  update public.clubes
  set usuario_dono_id = p_usuario_id
  where id = p_clube_id;

  if p_usuario_id is not null then
    update public.usuarios
    set clube_id = p_clube_id
    where id = p_usuario_id;
  end if;
end;
$$;

create or replace function public.admin_iniciar_draft(p_ordem_clubes bigint[])
returns void
language plpgsql
security definer
set search_path = public
set row_security = off
as $$
declare
  total_clubes integer;
begin
  if not public.usuario_eh_admin() then
    raise exception 'Apenas o administrador pode iniciar o draft'
      using errcode = '42501';
  end if;

  select count(*) into total_clubes from public.clubes;
  if total_clubes < 2
    or cardinality(p_ordem_clubes) <> total_clubes
    or (
      select count(distinct clube_id)
      from unnest(p_ordem_clubes) as ordem(clube_id)
    ) <> total_clubes
    or exists (
      select 1 from unnest(p_ordem_clubes) as ordem(clube_id)
      where not exists (select 1 from public.clubes c where c.id = clube_id)
    )
  then
    raise exception 'A ordem do draft deve conter todos os clubes uma única vez';
  end if;

  -- A condição explícita também funciona em bancos com proteção contra DELETE sem WHERE.
  delete from public.draft_contratacoes where id is not null;
  update public.draft_estado
  set iniciado = true,
      ordem_clubes = p_ordem_clubes,
      posicao_atual = 0,
      atualizado_em = now()
  where id = 1;
end;
$$;

create or replace function public.draft_contratar_jogador(
  p_nome text,
  p_posicao text,
  p_valor numeric
)
returns void
language plpgsql
security definer
set search_path = public
set row_security = off
as $$
declare
  estado public.draft_estado%rowtype;
  clube_atual_id bigint;
  jogador_id_criado bigint;
  numero_disponivel integer;
  orcamento_restante numeric;
begin
  select * into estado
  from public.draft_estado
  where id = 1
  for update;

  if not estado.iniciado then
    raise exception 'O draft não está iniciado';
  end if;

  clube_atual_id := estado.ordem_clubes[estado.posicao_atual + 1];
  if clube_atual_id is null then
    raise exception 'Posição atual do draft inválida';
  end if;

  if not public.usuario_eh_admin() and not exists (
    select 1 from public.clubes
    where id = clube_atual_id and usuario_dono_id = auth.uid()
  ) then
    raise exception 'Não é a vez do seu clube'
      using errcode = '42501';
  end if;

  if nullif(trim(p_nome), '') is null
    or p_posicao not in ('Goleiro', 'Zagueiro', 'Lateral', 'Meio-Campo', 'Atacante')
    or p_valor < 0
  then
    raise exception 'Dados do jogador inválidos';
  end if;

  select c.orcamento - coalesce(sum(dc.valor), 0)
  into orcamento_restante
  from public.clubes c
  left join public.draft_contratacoes dc on dc.clube_id = c.id
  where c.id = clube_atual_id
  group by c.orcamento;

  if p_valor > orcamento_restante then
    raise exception 'Orçamento insuficiente';
  end if;

  select numero into numero_disponivel
  from generate_series(1, 99) as numeros(numero)
  where not exists (
    select 1 from public.jogadores j
    where j.clube_id = clube_atual_id and j.numero = numero
  )
  order by numero
  limit 1;

  if numero_disponivel is null then
    raise exception 'O clube não possui número de camisa disponível';
  end if;

  insert into public.jogadores (
    nome, posicao, numero, clube_id, foto_url, status,
    valor_mercado, jogos_suspensao
  ) values (
    trim(p_nome), p_posicao, numero_disponivel, clube_atual_id, null, 'ativo',
    p_valor, 0
  ) returning id into jogador_id_criado;

  insert into public.draft_contratacoes (
    jogador_id, jogador_nome, clube_id, valor, rodada
  ) values (
    jogador_id_criado, trim(p_nome), clube_atual_id, p_valor,
    estado.posicao_atual + 1
  );

  update public.draft_estado
  set posicao_atual = estado.posicao_atual + 1,
      iniciado = estado.posicao_atual + 1 < cardinality(estado.ordem_clubes),
      atualizado_em = now()
  where id = 1;
end;
$$;

create or replace function public.criar_proposta_transferencia(
  p_jogador_id bigint,
  p_clube_destino_id bigint,
  p_valor numeric,
  p_tipo text,
  p_jogador_troca_id bigint default null,
  p_mensagem text default ''
)
returns bigint
language plpgsql
security definer
set search_path = public
set row_security = off
as $$
declare
  clube_origem_id bigint;
  proposta_id bigint;
  orcamento_destino numeric;
begin
  select clube_id into clube_origem_id
  from public.jogadores
  where id = p_jogador_id and status in ('ativo', 'suspenso');

  if clube_origem_id is null or clube_origem_id = p_clube_destino_id then
    raise exception 'Jogador indisponível para transferência';
  end if;

  if not public.usuario_eh_admin() and not exists (
    select 1 from public.clubes
    where id = p_clube_destino_id and usuario_dono_id = auth.uid()
  ) then
    raise exception 'Você não representa o clube comprador'
      using errcode = '42501';
  end if;

  select orcamento into orcamento_destino
  from public.clubes where id = p_clube_destino_id;
  if p_valor < 0 or p_valor > orcamento_destino then
    raise exception 'Orçamento insuficiente';
  end if;

  if p_jogador_troca_id is not null and not exists (
    select 1 from public.jogadores
    where id = p_jogador_troca_id and clube_id = p_clube_destino_id
  ) then
    raise exception 'Jogador oferecido na troca não pertence ao clube comprador';
  end if;

  insert into public.transferencias (
    jogador_id, clube_origem_id, clube_destino_id, valor, tipo,
    jogador_troca_id, data, status, mensagem
  ) values (
    p_jogador_id, clube_origem_id, p_clube_destino_id, p_valor, p_tipo,
    p_jogador_troca_id, current_date, 'pendente', coalesce(p_mensagem, '')
  ) returning id into proposta_id;

  return proposta_id;
end;
$$;

create or replace function public.aceitar_proposta_transferencia(p_transferencia_id bigint)
returns void
language plpgsql
security definer
set search_path = public
set row_security = off
as $$
declare
  proposta public.transferencias%rowtype;
  orcamento_comprador numeric;
begin
  select * into proposta
  from public.transferencias
  where id = p_transferencia_id
  for update;

  if proposta.id is null or proposta.status <> 'pendente' then
    raise exception 'Proposta não encontrada ou já respondida';
  end if;

  if not public.usuario_eh_admin() and not exists (
    select 1 from public.clubes
    where id = proposta.clube_origem_id and usuario_dono_id = auth.uid()
  ) then
    raise exception 'Apenas o clube vendedor pode aceitar a proposta'
      using errcode = '42501';
  end if;

  if not exists (
    select 1 from public.jogadores
    where id = proposta.jogador_id and clube_id = proposta.clube_origem_id
  ) then
    raise exception 'O jogador não pertence mais ao clube vendedor';
  end if;

  select orcamento into orcamento_comprador
  from public.clubes where id = proposta.clube_destino_id
  for update;
  if proposta.valor > orcamento_comprador then
    raise exception 'O clube comprador não possui orçamento suficiente';
  end if;

  if proposta.jogador_troca_id is not null then
    update public.jogadores
    set clube_id = proposta.clube_origem_id
    where id = proposta.jogador_troca_id
      and clube_id = proposta.clube_destino_id;
    if not found then
      raise exception 'O jogador oferecido na troca não está mais disponível';
    end if;
  end if;

  update public.jogadores
  set clube_id = proposta.clube_destino_id
  where id = proposta.jogador_id;

  if proposta.valor > 0 then
    update public.clubes
    set orcamento = orcamento + proposta.valor
    where id = proposta.clube_origem_id;
    update public.clubes
    set orcamento = orcamento - proposta.valor
    where id = proposta.clube_destino_id;
  end if;

  update public.transferencias
  set status = 'aceita'
  where id = p_transferencia_id;
end;
$$;

