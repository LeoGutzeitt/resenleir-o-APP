create table if not exists public.usuarios (
  id uuid primary key references auth.users(id) on delete cascade,
  email text not null,
  nome text not null,
  role text not null check (role in ('admin', 'dono')),
  clube_id bigint references public.clubes(id)
);

create table if not exists public.draft_estado (
  id smallint primary key default 1 check (id = 1),
  iniciado boolean not null default false,
  ordem_clubes bigint[] not null default '{}',
  posicao_atual integer not null default 0,
  atualizado_em timestamptz not null default now()
);

create table if not exists public.draft_contratacoes (
  id bigint generated by default as identity primary key,
  jogador_id bigint not null references public.jogadores(id) on delete cascade,
  jogador_nome text not null,
  clube_id bigint not null references public.clubes(id) on delete cascade,
  valor numeric not null check (valor >= 0),
  rodada integer not null,
  criado_em timestamptz not null default now(),
  unique (jogador_id)
);

