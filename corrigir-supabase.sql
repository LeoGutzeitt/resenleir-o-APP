-- Remove também políticas antigas com nomes não previstos. Uma política
-- residual em usuarios era suficiente para reintroduzir recursão em todo app.
do $$
declare
  politica record;
begin
  for politica in
    select schemaname, tablename, policyname
    from pg_policies
    where schemaname = 'public'
      and tablename = any (array[
        'usuarios', 'clubes', 'jogadores', 'jogos', 'estatisticas',
        'transferencias', 'noticias', 'draft_estado', 'draft_contratacoes'
      ])
  loop
    execute format(
      'drop policy %I on %I.%I',
      politica.policyname,
      politica.schemaname,
      politica.tablename
    );
  end loop;
end;
$$;

