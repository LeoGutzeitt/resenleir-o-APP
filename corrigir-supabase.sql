create or replace function public.admin_gerar_jogos()
returns integer
language plpgsql
security definer
set search_path = public
set row_security = off
as $$
declare
  times bigint[];
  quantidade integer;
  rodada integer;
  partida integer;
  clube_casa bigint;
  clube_fora bigint;
  total_criado integer := 0;
begin
  if not public.usuario_eh_admin() then
    raise exception 'Apenas o administrador pode gerar os jogos'
      using errcode = '42501';
  end if;

  if exists (select 1 from public.jogos where fase = 'grupos') then
    raise exception 'Os jogos da fase de grupos já foram gerados';
  end if;

  select array_agg(id order by id) into times from public.clubes;
  if coalesce(cardinality(times), 0) < 2 then
    raise exception 'Cadastre pelo menos dois clubes';
  end if;

  if cardinality(times) % 2 = 1 then
    times := array_append(times, null::bigint);
  end if;
  quantidade := cardinality(times);

  for rodada in 1..(quantidade - 1) loop
    for partida in 1..(quantidade / 2) loop
      clube_casa := times[partida];
      clube_fora := times[quantidade - partida + 1];
      if clube_casa is not null and clube_fora is not null then
        insert into public.jogos (
          clube_casa_id, clube_fora_id, gols_casa, gols_fora,
          rodada, fase, data, status
        ) values (
          clube_casa, clube_fora, null, null, rodada, 'grupos',
          current_date + ((rodada - 1) * 7), 'agendado'
        );
        -- Segundo turno: os mesmos confrontos com mando invertido.
        insert into public.jogos (
          clube_casa_id, clube_fora_id, gols_casa, gols_fora,
          rodada, fase, data, status
        ) values (
          clube_fora, clube_casa, null, null, rodada + (quantidade - 1), 'grupos',
          current_date + ((rodada + quantidade - 2) * 7), 'agendado'
        );
        total_criado := total_criado + 2;
      end if;
    end loop;

    times := array[times[1], times[quantidade]] || times[2:(quantidade - 1)];
  end loop;

  return total_criado;
end;
$$;

-- Limpa somente a competição: resultados e estatísticas. Clubes, donos, elencos,
-- valores de mercado, transferências e o histórico do draft são preservados.
create or replace function public.admin_resetar_competicao()
returns void
language plpgsql
security definer
set search_path = public
set row_security = off
as $$
begin
  if not public.usuario_eh_admin() then
    raise exception 'Apenas o administrador pode limpar a competição' using errcode = '42501';
  end if;

  delete from public.estatisticas where id is not null;
  delete from public.jogos where id is not null;
end;
$$;

