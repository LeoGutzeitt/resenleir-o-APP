-- Corrige defaults que impediam inserções feitas pelo aplicativo.
-- Algumas instalações já usam IDENTITY; nesse caso o próprio banco já gera o ID.
DO $$
DECLARE item record;
BEGIN
  FOR item IN
    SELECT c.relname AS tabela, a.attname AS coluna, a.attidentity, format_type(a.atttypid, a.atttypmod) AS tipo
    FROM pg_attribute a JOIN pg_class c ON c.oid = a.attrelid JOIN pg_namespace n ON n.oid = c.relnamespace
    WHERE n.nspname = 'public' AND a.attnum > 0 AND NOT a.attisdropped
      AND c.relname IN ('jogadores','jogos','estatisticas','transferencias','noticias') AND a.attname = 'id'
  LOOP
    IF item.attidentity = '' AND item.tipo = 'uuid' THEN
      EXECUTE format('ALTER TABLE public.%I ALTER COLUMN id SET DEFAULT gen_random_uuid()', item.tabela);
    END IF;
  END LOOP;
END $$;

DO $$
DECLARE identidade text; possui_default boolean;
BEGIN
  SELECT a.attidentity, (pg_get_expr(d.adbin, d.adrelid) IS NOT NULL)
    INTO identidade, possui_default
  FROM pg_attribute a
  JOIN pg_class c ON c.oid = a.attrelid
  JOIN pg_namespace n ON n.oid = c.relnamespace
  LEFT JOIN pg_attrdef d ON d.adrelid = a.attrelid AND d.adnum = a.attnum
  WHERE n.nspname = 'public' AND c.relname = 'clubes' AND a.attname = 'id';
  IF identidade = '' AND NOT possui_default THEN
    ALTER TABLE public.clubes ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY;
  END IF;
END $$;

